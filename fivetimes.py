print('My name is')
i = 0
while i < 5:
    print('Jimmy Five Times ' + str(i))
    i = i + 1

print('\nStart at 12, count to 15')
print('My name is')
for i in range(12, 16):
    print('Jimmy Five Times ' + str(i))

print('\nStart at Zero, Count by Two')
print('My name is')
for i in range(0, 10, 2):
    print('Jimmy Five Times ' + str(i))

print('\nCount Down from Five')
print('My name is')
for i in range(5, -1, -1):
    print('Jimmy Five Times ' + str(i))
